    private void bNew() {
        employeeCode.setText(null);
        employeeName.setText(null);
        sexM.setSelected(true);
        sexF.setSelected(false);
        sexU.setSelected(false);
        department.setSelectedIndex(0);
        salary.setText(null);
        employeeCode.requestFocus();
    }

